package com.scottejames.downsman.model;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import com.amazonaws.services.dynamodbv2.document.Item;
import com.scottejames.downsman.utils.HashHelper;

import java.sql.Ref;

@DynamoDBTable(tableName = "User")
public class UserModel {

    private String username = null;
    private String password = null;
    private String email = null;
    private String mobile = null;

    private Boolean admin = false;
    private String id = null;
    public UserModel(){

    }
    public UserModel(String u, String p) {
        username = u;
        if (p!=null)
            password = HashHelper.hashPassword(p);
    }

    public UserModel(String u, String p,String phone,String email) {
        username = u;
        if (p!=null)
            password = HashHelper.hashPassword(p);
         mobile = phone;
         this.email = email;
    }
    @DynamoDBRangeKey
    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }

    @DynamoDBHashKey
    public String getOwnerID() { return "None"; }
    public void setOwnerID(String ownerID) {;}

    @DynamoDBAttribute
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }

    @DynamoDBAttribute
    public Boolean isAdmin() {
        return admin;
    }
    public void setAdmin(Boolean admin) {
        this.admin = admin;
    }

    @DynamoDBAttribute
    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    @DynamoDBAttribute
    public String getMobile() { return mobile; }
    public void setMobile(String mobile) { this.mobile = mobile; }

    @DynamoDBAutoGeneratedKey
    public String getId(){ return id;}
    public void setId(String id){ this.id = id;}

    @DynamoDBIgnore
    public boolean isPersisted() {
        if (id == null) return false;
        else return true;
    }

    public String toString(){
        return " U : " + username + " P : " + password;
    }


}
